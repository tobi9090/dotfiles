function theme.powerline_rl(cr, width, height)
    local arrow_depth, offset = height/2, 0

    -- Avoid going out of the (potential) clip area
    if arrow_depth < 0 then
        width  =  width + 2*arrow_depth
        offset = -arrow_depth
    end

    cr:move_to(offset + arrow_depth         , 0        )
    cr:line_to(offset + width               , 0        )
    cr:line_to(offset + width - arrow_depth , height/2 )
    cr:line_to(offset + width               , height   )
    cr:line_to(offset + arrow_depth         , height   )
    cr:line_to(offset                       , height/2 )

    cr:close_path()
end

local function pl(widget, bgcolor, padding)
    return wibox.container.background(wibox.container.margin(widget, dpi(16), dpi(16)), bgcolor, theme.powerline_rl)
end

function theme.at_screen_connect(s)
    -- Quake application
    s.quake = lain.util.quake({ app = awful.util.terminal })

    -- If wallpaper is a function, call it with the screen
    local wallpaper = theme.wallpaper
    if type(wallpaper) == "function" then
        wallpaper = wallpaper(s)
    end
    gears.wallpaper.maximized(wallpaper, s, true)

    -- Tags
    awful.tag(awful.util.tagnames, s, awful.layout.layouts[1])

 
    -- Create an imagebox widget which will contains an icon indicating which layout we're using.
    -- We need one layoutbox per screen.
    s.mylayoutbox = awful.widget.layoutbox(s)
    s.mylayoutbox:buttons(my_table.join(
                           awful.button({}, 1, function () awful.layout.inc( 1) end),
                           awful.button({}, 2, function () awful.layout.set( awful.layout.layouts[1] ) end),
                           awful.button({}, 3, function () awful.layout.inc(-1) end),
                           awful.button({}, 4, function () awful.layout.inc( 1) end),
                           awful.button({}, 5, function () awful.layout.inc(-1) end)))
    -- Create a taglist widget
    s.mytaglist = awful.widget.taglist(s, awful.widget.taglist.filter.all, awful.util.taglist_buttons)

    -- Create a tasklist widget
    s.mytasklist = awful.widget.tasklist(s, awful.widget.tasklist.filter.focused)

    -- Create the wibox
    s.mywibox = awful.wibar({
		height = dpi(30),
		bg = theme.bg_normal,
		opacity = 0.9,
		width = s.geometry.width - 20,
		shape = theme.border_rounded,
		screen = s,
	})

    -- Add widgets to the wibox
    s.mywibox:setup ({
        {
        	layout = wibox.layout.align.horizontal,
		{ -- Left widgets
		    layout = wibox.layout.fixed.horizontal,
		    s.mytaglist,		    
		},
		{
		s.mytasklist, -- Middle widget
		widget = wibox.container.place,
		},
		{ -- Right widgets
		    layout = wibox.layout.fixed.horizontal,
		    --wibox.widget.systray(),
		    arrow(theme.bg_normal, "#777E76"),
		    wibox.container.background(wibox.container.margin(wibox.widget { memicon, mem.widget, layout = wibox.layout.align.horizontal }, dpi(2), dpi(3)), "#777E76"),
		    arrow("#777E76", "#4B696D"),
		    wibox.container.background(wibox.container.margin(wibox.widget { cpuicon, cpu.widget, layout = wibox.layout.align.horizontal }, dpi(3), dpi(4)), "#4B696D"),
		    arrow("#4B696D", "#4B3B51"),
		    wibox.container.background(wibox.container.margin(wibox.widget { tempicon, temp.widget, layout = wibox.layout.align.horizontal }, dpi(4), dpi(4)), "#4B3B51"),
		    arrow("#4B3B51", "#d79921"),
		    wibox.container.background(wibox.container.margin(wibox.widget { nil, neticon, net.widget, layout = wibox.layout.align.horizontal }, dpi(3), dpi(3)), "#d79921"),
		    arrow("#d79921", "#CB755B"),
		    wibox.container.background(wibox.container.margin(time.widget, dpi(4), dpi(8)), "#CB755B"),
		    arrow("#CB755B", "alpha"),
		    
		    s.mylayoutbox,
 
		},
	},
	left = dpi(15),
	right = dpi(15),
	widget = wibox.container.margin,
       
    })
end

return theme
